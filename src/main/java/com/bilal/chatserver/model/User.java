package com.bilal.chatserver.model;
/**
 *
 * @author baloch
 */
public class User {
    private int id;
    private String name;
    private String status;

    public User() {
    }
    
    public int getId() {
        return id;
    }
    
    public String getName() {
        return name;
    }

    public String getStatus() {
        return status;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    public void setName(String name) {
        this.name = name;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
